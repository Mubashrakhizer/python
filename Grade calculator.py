
marks = int(input("Enter the marks for grade : "))
if 80 <= marks <= 100 :
  print("You got A grade")
if 70 <= marks <= 80 :
  print("You got B grade")
if 60 <= marks <= 70 :
  print("You got C grade")
if 50 <= marks <= 60 :
  print("You got D grade")
if 40 <= marks <= 50 :
  print("You got E grade")
if 0 <= marks <= 40 :
  print("You are failed")
